const showNames = function (list) {
  list.forEach((element, i) => {
    setTimeout(() => {
      let randomNumber = Math.floor(100 + (20000 - 100) * Math.random());

      const html = `Someone from ${element} just earned <br> $ ${randomNumber}`;

      message.innerHTML = html
      message.style.display = 'block';

      setTimeout(function(){
        message.style.display = 'none'; 
      }, 3000);      


    }, i * 7000);
  });
};

